    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String BloodGroup=(String)jComboBox2.getSelectedItem();
        String Units=jTextField1.getText();
        int unit=Integer.parseInt(Units);
        try {
            Connection con=MyCNX.getConnection();
            Statement st = con.createStatement();
            st.executeUpdate ("update Stock set Units=Units-'"+Units+"'where BloodGroup='"+BloodGroup+"'");
            JOptionPane.showMessageDialog(null,"Successfully Updated");
            setVisible(false);
            new StockDecrease().setVisible(true);

        }
        catch (SQLException e) {

            {
                JOptionPane.showMessageDialog(null,e);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

